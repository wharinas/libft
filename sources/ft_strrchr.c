char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ptr;

	i = -1;
	ptr = 0;
	c %= 256;
	while (s[++i])
		if (s[i] == c)
			ptr = (char *)(s + i);
	if (s[i] == c)
		ptr = (char *)(s + i);
	return (ptr);
}
